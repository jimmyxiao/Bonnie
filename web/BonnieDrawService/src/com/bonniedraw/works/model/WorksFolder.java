package com.bonniedraw.works.model;

public class WorksFolder {
    private Integer worksFolderId;

    private Integer userFolderId;

    private Integer worksId;

    public Integer getWorksFolderId() {
        return worksFolderId;
    }

    public void setWorksFolderId(Integer worksFolderId) {
        this.worksFolderId = worksFolderId;
    }

    public Integer getUserFolderId() {
        return userFolderId;
    }

    public void setUserFolderId(Integer userFolderId) {
        this.userFolderId = userFolderId;
    }

    public Integer getWorksId() {
        return worksId;
    }

    public void setWorksId(Integer worksId) {
        this.worksId = worksId;
    }
}