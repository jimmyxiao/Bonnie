public class NoticeInfo {
    private int notMsgId;
    private String msg;
    private int msgType;
    private int userIdFollow;
    private String userNameFollow;
    private int workId;
    private String title;
    private String imagePath;
    private int workMsgId;
    private String workMsg;
    private String creationDate;

    public int getNotMsgId() {
        return notMsgId;
    }

    public void setNotMsgId(int notMsgId) {
        this.notMsgId = notMsgId;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getMsgType() {
        return msgType;
    }

    public void setMsgType(int msgType) {
        this.msgType = msgType;
    }

    public int getUserIdFollow() {
        return userIdFollow;
    }

    public void setUserIdFollow(int userIdFollow) {
        this.userIdFollow = userIdFollow;
    }

    public String getUserNameFollow() {
        return userNameFollow;
    }

    public void setUserNameFollow(String userNameFollow) {
        this.userNameFollow = userNameFollow;
    }

    public int getWorkId() {
        return workId;
    }

    public void setWorkId(int workId) {
        this.workId = workId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public int getWorkMsgId() {
        return workMsgId;
    }

    public void setWorkMsgId(int workMsgId) {
        this.workMsgId = workMsgId;
    }

    public String getWorkMsg() {
        return workMsg;
    }

    public void setWorkMsg(String workMsg) {
        this.workMsg = workMsg;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(String creationDate) {
        this.creationDate = creationDate;
    }
}
