import UIKit

extension UIColor {
    static func getTextColor() -> UIColor {
        return UIColor(red: 0.36, green: 0.36, blue: 0.36, alpha: 1.0)
    }

    static func getAccentColor() -> UIColor {
        return UIColor(red: 0.89, green: 0.33, blue: 0.37, alpha: 1.0)
    }
}
