import UIKit

extension UIColor {
    static func getTextColor() -> UIColor {
        return UIColor(red: 0.27, green: 0.20, blue: 0.20, alpha: 1.0)
    }

    static func getAccentColor() -> UIColor {
        return UIColor(red: 0.95, green: 0.44, blue: 0.27, alpha: 1.0)
    }
}
